function [dX, constraints, signals, param] = liuDiesel2(X, U, N_ice, param)
% LIUDIESEL2(X, U, N_ice, param)
% LiU Diesel 2 - A MVEM of a CI, 6 cylinder in-line, 2450 Nm, 450 bhp
% engine.
